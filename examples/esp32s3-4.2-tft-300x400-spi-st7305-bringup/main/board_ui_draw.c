static const uint8_t *glyph_rows(char c)
{
    static const uint8_t blank[7] = {0};

    if (c < ' ' || c > 'Z') {
        return blank;
    }
    return s_font_5x7[c - ' '];
}

void board_ui_draw_glyph(uint16_t x, uint16_t y, char c, uint16_t scale, uint16_t color)
{
    const uint8_t *rows = glyph_rows(c);

    for (uint16_t ry = 0; ry < 7; ++ry) {
        for (uint16_t rx = 0; rx < 5; ++rx) {
            if (rows[ry] & (1U << (4 - rx))) {
                st7305_lcd_fill_rect(x + rx * scale,
                                     y + ry * scale,
                                     scale,
                                     scale,
                                     color);
            }
        }
    }
}

uint16_t board_ui_text_width(const char *text, uint16_t scale)
{
    if (text == NULL || text[0] == '\0') {
        return 0;
    }
    const uint16_t char_w = 5 * scale + scale;
    return (uint16_t)(strlen(text) * char_w);
}

void board_ui_draw_text(uint16_t x, uint16_t y, const char *text, uint16_t scale, uint16_t color)
{
    if (text == NULL) {
        return;
    }

    const uint16_t char_w = 5 * scale + scale;
    uint16_t pen_x = x;
    for (size_t i = 0; text[i] != '\0'; ++i) {
        board_ui_draw_glyph(pen_x, y, text[i], scale, color);
        pen_x += char_w;
    }
}
